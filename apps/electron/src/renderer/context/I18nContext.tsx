interface I18nContextValue {
  language: AppLanguage
  setLanguage: (language: AppLanguage) => Promise<void>
  t: (key: string, values?: InterpolationValues) => string
  te: (text: string, values?: InterpolationValues) => string
}

const I18nContext = createContext<I18nContextValue | null>(null)

function interpolate(template: string, values?: InterpolationValues): string {
  if (!values) return template
  return template.replace(/\{\{(\w+)\}\}/g, (_, key: string) => {
    const value = values[key]
    return value === undefined ? '' : String(value)
  })
}

export function I18nProvider({ children }: { children: React.ReactNode }) {
  const [language, setLanguageState] = useState<AppLanguage>(DEFAULT_LANGUAGE)

  useEffect(() => {
    let mounted = true
    window.electronAPI.getAppLanguage()
      .then((lang) => {
        if (!mounted) return
        setLanguageState(normalizeLanguage(lang))
      })
      .catch((error) => {
        console.error('[i18n] Failed to load app language:', error)
      })
    return () => {
      mounted = false
    }
  }, [])

  useEffect(() => {
    document.documentElement.lang = language
  }, [language])

  const setLanguage = useCallback(async (nextLanguage: AppLanguage) => {
    const normalized = normalizeLanguage(nextLanguage)
    setLanguageState(normalized)
    try {
      await window.electronAPI.setAppLanguage(normalized)
    } catch (error) {
      console.error('[i18n] Failed to persist app language:', error)
    }
  }, [])

  const t = useCallback((key: string, values?: InterpolationValues) => {
    const languageMessages = MESSAGES[language] ?? MESSAGES.en
    const message = languageMessages[key] ?? MESSAGES.en[key] ?? key
    return interpolate(message, values)
  }, [language])

  const te = useCallback((text: string, values?: InterpolationValues) => {
    const languageTexts = TEXT_TRANSLATIONS[language] ?? TEXT_TRANSLATIONS.en
    const message = languageTexts[text] ?? text
    return interpolate(message, values)
  }, [language])

  const value = useMemo(() => ({ language, setLanguage, t, te }), [language, setLanguage, t, te])

  return (
    <I18nContext.Provider value={value}>
      {children}
    </I18nContext.Provider>
  )
}

export function useI18n() {
  const context = useContext(I18nContext)
  if (!context) {
    throw new Error('useI18n must be used within I18nProvider')
  }
  return context
}
